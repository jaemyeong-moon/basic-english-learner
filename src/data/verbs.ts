import { Verb, QuizQuestion, Example } from "@/types";

type ExampleGroup = {
  pattern: string;
  highlight: string;
  pairs: [string, string][];
};

function buildExamples(verbId: string, groups: ExampleGroup[]): Example[] {
  const result: Example[] = [];
  let counter = 0;
  for (const g of groups) {
    for (const [english, korean] of g.pairs) {
      counter++;
      result.push({
        id: `${verbId}-${counter}`,
        english,
        korean,
        pattern: g.pattern,
        highlight: g.highlight,
      });
    }
  }
  return result;
}

